package khanghv.dtos;

import java.io.Serializable;

/**
 *
 * @author USER
 */
public class ErrorToolObj implements Serializable{
    String idError, nameError, quanError, imgError, descError, statusError;

    public ErrorToolObj() {
    }

    public String getIdError() {
        return idError;
    }

    public void setIdError(String idError) {
        this.idError = idError;
    }

    public String getNameError() {
        return nameError;
    }

    public void setNameError(String nameError) {
        this.nameError = nameError;
    }

    public String getQuanError() {
        return quanError;
    }

    public void setQuanError(String quanError) {
        this.quanError = quanError;
    }

    public String getImgError() {
        return imgError;
    }

    public void setImgError(String imgError) {
        this.imgError = imgError;
    }

    public String getDescError() {
        return descError;
    }

    public void setDescError(String descError) {
        this.descError = descError;
    }

    public String getStatusError() {
        return statusError;
    }

    public void setStatusError(String statusError) {
        this.statusError = statusError;
    }
    
    
            
    
}
